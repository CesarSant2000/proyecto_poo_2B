    private void btnLCotizzacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLCotizzacionActionPerformed
	    if (cmbProvincias.getSelectedIndex() != 0) {
		    String provincia = cmbProvincias.getSelectedItem().toString();
		    boolean RAMSeleccionada = llenarRAM(provincia);
		    boolean almacenamientoSeleccionado = llenarAlmacenamiento(provincia);
		    boolean procesadorSeleccionado = llenarProcesador(provincia);

		    boolean llenadoExitoso = RAMSeleccionada
			    || almacenamientoSeleccionado || procesadorSeleccionado;
		    if (!llenadoExitoso) {
			    String warning = "No se ha seleccionado ningun campo";
			    JOptionPane.showMessageDialog(rootPane, warning, "Advertencia", JOptionPane.WARNING_MESSAGE);
		    } else {
			    GestionPresentacion nuevaVentana = new GestionPresentacion(productosDeseados);
			    nuevaVentana.setVisible(true);
			    this.setVisible(false);
		    }
	    } else {
		    String error = "Seleccione una provincia";
		    JOptionPane.showMessageDialog(rootPane, error, "Provincia no seleccionada", JOptionPane.ERROR_MESSAGE);
	    }
    }//GEN-LAST:event_btnLCotizzacionActionPerformed

